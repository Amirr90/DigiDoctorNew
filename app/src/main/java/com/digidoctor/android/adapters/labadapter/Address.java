package com.digidoctor.android.adapters.labadapter;

public class Address {
    String memberId;
    String name;
    String mobileNo;
    String houseNo;
    String area;
    String pincode;
    String state;
    String city;
    String locality;
    Boolean isDefault;
    String addressType;
    String isSundayOpen;
    String isSaturdayOpen;
    String addressId;

    public String getMemberId() {
        return memberId;
    }

    public String getName() {
        return name;
    }

    public String getMobileNo() {
        return mobileNo;
    }

    public String getHouseNo() {
        return houseNo;
    }

    public String getArea() {
        return area;
    }

    public String getPincode() {
        return pincode;
    }

    public String getState() {
        return state;
    }

    public String getCity() {
        return city;
    }

    public String getLocality() {
        return locality;
    }

    public Boolean getIsDefault() {
        return isDefault;
    }

    public String getAddressType() {
        return addressType;
    }

    public String getIsSundayOpen() {
        return isSundayOpen;
    }

    public String getIsSaturdayOpen() {
        return isSaturdayOpen;
    }

    public String getAddressId() {
        return addressId;
    }
}
